         <p id="p" align="left">По свидетельству сохранившихся исторических документов упоминание о дорожной деятельности на территории области относится ещё к временам царской России. Особенно активизировалась дорожная деятельность в 1944 году. Для восстановления разрушенных в результате военных действий автомобильных дорог и мостов Брянской области было образовано Брянское областное управление автомобильного транспорта. Одновременно решением 1-й сессии Брянского облисполкома был создан Брянский областной дорожный отдел.</p>
         <p id="p" align="left">В 1953 году путем слияния Дорожного отдела с областным управлением автотранспорта образовалось Управление дорожного и транспортного хозяйства Брянского облисполкома, которое в 1954 году согласно Указу Президиума Верховного Совета РСФСР от 12.04.1954 года и решения Брянского облисполкома от 16.11.1954 года № 566 переименовано в Брянское областное управление автомобильного транспорта и шоссейных дорог.</p>
	     <img style="margin-left: 10px" id="rukovod" src="../images/rukovodstvo/hist2.jpg"  width="230" height="180" class="rightimg">
