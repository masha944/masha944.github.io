               <p id="p" align="left">На основании приказа Министерства автомобильных дорог РСФСР от 13.12.1987 года № 204-ор в 1988 году Брянское областное производственное управление строительства и эксплуатации автомобильных дорог «Брянскавтодор» было преобразовано в Брянское областное проектно–ремонтно-строительное объединение автомобильных дорог (ПРСО «Брянскавтодор»), в период с 1991 по 1994гг. – Брянское государственное проектно-ремонтно-строительное предприятие автомобильных дорог «Брянскавтодор» по решению Брянского облисполкома от 22.04.1991 года № 178.</p> 
