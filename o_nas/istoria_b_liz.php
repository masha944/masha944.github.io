          <p id="p" align="left">ПАНКРАТОВ Евгений Ефимович начальник Брянского областного производственного управления строительства и эксплуатации автомобильных дорог «Брянскавтодор» с мая 1981 года по июнь 1996 года.</p>     
          <p id="p" align="left">Огромное внимание Евгений Ефимович уделял развитию производственных баз. За время работы под руководством Евгения Ефимовича во всех районах Брянской области образовано 27 производственных баз и асфальтобетонных заводов с повышенными железнодорожными тупиками.</p>
          <p id="p" align="left">Неоценимый вклад Евгений Ефимович внес в развитие автомобильных дорог Брянской области, под его руководством в 1985 году построено и реконструировано 250 км автомобильных дорог. Предприятие награждено Переходящим Красным знаменем, которое вручил лично Министр автомобильных дорог Брухнов В.А.</p>
               <p id="p" align="left">За вклад в развитие строительства на Брянщине Панкратов Евгений Ефимович награжден: Орденом трудового Красного знамени за ликвидацию последствий аварии на Чернобыльской АЭС; Знаком Почета за преобразование нечерноземной зоны Российской Федерации; Знаком Почета за выполнение плана пятилетки; удостоен почетного звания «Заслуженный строитель РСФСР»; награжден нагрудным знаком «Почетный дорожник».ГОРБАТКОВ Филипп Дмитриевич</p> 
            <img style="margin-right: 10px" id="rukovod" src="../images/rukovodstvo/Gorbatkov.jpg"  width="300" height="430" class="leftimg">
          <p id="p" align="left">ГОРБАТКОВ Филипп Дмитриевич начальник управления автомобильных дорог администрации Брянской области с апреля 1994 года по январь 2001 года. В 1995-1999 годах под руководством Филиппа Дмитриевича высокими темпами велось строительство объектов, имеющих важное значение для социально-экономического развития Брянской области: построен участок Северо-Западного обхода города Брянска протяженностью 3,5 км с мостом через реку Десна и путепроводом через железную дорогу; в 2001 году окончено строительство Северо-Западного обхода с вводом транспортных развязок на федеральную автомобильную дорогу «Брянск-Смоленск» и автодорогу «Брянск-Дятьково-граница Калужской области»; построена автомобильная дорога «Селиловичи-Березовка-Шаровичи» с мостовым переходом через реку Десна; автомобильная дорога «Суземка-станция Трубчевск» в Суземском и Трубчевском районах; реконструирована автомобильная дорога «Брянск-Новозыбков-Погар» и другие объекты.</p>  
          <p id="p" align="left">За неоценимый вклад в развитие строительства на Брянщине Горбатков Филипп Дмитриевич удостоен Почетного звания «Заслуженный строитель Российской Федерации».</p>  
          <p id="p" align="left">СИМОНОВ Александр Алексеевич работал в Управлении автомобильных дорог администрации Брянской области в период с января 2001 по июнь 2002 года (в апреле 2002 года Управление автомобильных дорог администрации Брянской области было реорганизовано в ГУ "Управление дорожного хозяйства Брянской области"), продолжил работу с января 2003 по март 2005 года. </p>
          <p id="p" align="left">При руководстве Симонова А.А. в 2002 году была проведена реорганизация существующей системы дорожного хозяйства Брянской области. Началась активная работа по исследованию и внедрению новых технологических процессов. Смонтирована и внедрена в эксплуатацию установка для мойки щебня, применяемого при производстве "ямочного ремонта" дорог, запущена в эксплуатацию итальянская установка по приготовлению битумной эмульсии и на ее основе отработана новая технология по ремонту дорог. Приобретены высокотехнологичные белорусские машины "БЕЛТА", предназначенные для комплексного "ямочного ремонта" дорог. Проводился комплекс мероприятий по внедрению технологии устройства защитных слоев дорог с применением смесей типа "Слари Сил" установкой MACROPAIVER 12 B.</p>
          <p id="p" align="left">Активно проводилась работа по привлечению в отрасль молодых специалистов.</p>
          <p id="p" align="left">Под непосредственным руководством Александра Алексеевича построены автомобильные дороги: Хотылёво-Стаево в Брянском районе, п.  Московский-п.  Первомайский в Почепском районе, участки автомобильной дороги Холмечь-Тарасовка-Кокоревка в Брасовском районе, мостовой переход через реку Пес на автомобильной дороге "Колодня-Синьково"-Горицы в Жирятинском районе.</p>
