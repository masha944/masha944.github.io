			<p id="p" align="left">МЕРКУЛОВ Егор Павлович работал в ГУ "Управление дорожного хозяйства" с апреля 2005 года по апрель 2008 года.</p>
            <p id="p" align="left">За небольшой промежуток времени Меркуловым Е.П. проведена работа по совершенствованию структуры учреждения. Переработана система взаимодействия с подрядными организациями с учётом положений Федерального закона "О размещении заказов на поставку товаров, выполнение работ, оказание услуг для государственных и муниципальных нужд" от 21.07.2007 г. № 94-ФЗ", во главу угла поставлены вопросы качества и соблюдения сроков сдачи объектов. Удалось сократить сроки приведения в нормативное состояние дорог после зимнего периода. </p>
            <p id="p" align="left">Под руководством Егора Павловича внедрена новая технология: щебёночно-мастичный асфальтобетон. Большое внимание уделялось содержанию полос отвода автомобильных дорог.</p>
            <p id="p" align="left">Впервые, для определения объектов ремонта и строительства, автомобильных дорог по выделяемым субсидиям муниципальным образованиям осуществлялось взаимодействие с главами городских округов и муниципальных районов, депутатами различных уровней.</p>
            <p id="p" align="left">Под непосредственным руководством Егора Павлович впервые были разработаны: "Концепция развития автомобильных дорог Брянской области на 2008-2015 годы" и долгосрочная целевая программа "Модернизации транспортной системы Брянской области (2004-2010 годы), подпрограмма "Автомобильные дороги" (2004-2010 годы).</p>
            <p id="p" align="left">За период работы Меркулова Е.П. были построены автомобильные дороги: Свень-Стяжное в Брянском районе, Голубея-Бересток в Дубровском районе, "Брянск-Смоленск"-Никольская Слобода в Жуковском районе, Березовка-ж/д станция Нерусса в Суземском районе, другие участки автомобильных дорог. Начато строительство важных для региона автомобильных дорог: "Брянск-Новозыбков"-Погар-Гремяч (обход города Погар), Украина-Суземка-Добрунь, Хинель-Лемешевка в Севском районе, реконструкция автомобильной дороги Ковшовка-станция Свень в Брянском районе. </p>
