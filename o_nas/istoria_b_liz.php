         <p id="p" align="left">СУДОВСКИЙ Сергей Владимирович в период своей деятельности с 1962 по 1969 годы возглавлял дорожную отрасль Брянской области, которая при его участии выделена в отдельную структуру – Управление автомобильных дорог. Под его непосредственным руководством построен участок автомобильной дороги «Почеп-Гомель». Созданы новые дорожно-строительные организации в Брянске, Унече, Новозыбкове, Почепе, позднее в Климово, Стародубе, Погаре, Трубчевске, других районах области. При участии Сергея Владимировича сформирована опорная сеть Брянской области, построен ряд федеральных и региональных дорог с асфальтобетонным покрытием, которые и сегодня успешно функционируют на территории Брянской области. Началось строительство первых железобетонных мостов.</p>
         <p id="p" align="left">За многочисленные трудовые заслуги Сергей Владимирович награждался высокими Правительственными и иными наградами.
Сергей Владимирович оставил о себе добрую память среди дорожников и населения Брянской области.</p><br>
