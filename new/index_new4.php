Несомненно, большую важность имеет реконструкция автодороги «Жуковка-Косилово» в Жуковском районе, которая проходит через населенные пункты. Это второй выезд из города Жуковка в сторону города Дятьково и выезд на Москву, минуя Брянск. </p>
