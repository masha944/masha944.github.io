          <p id="p" align="left" style="position:relative; font-size:22px; line-height:1.5; font-family: 'Helvetica Neue';">На автодороге «Брянск-Смоленск»-Белоголовль, подрядчик – ООО «Дубровкаагропромдорстрой», отремонтирует более пяти километров автодороги. Отметим, что на данном участке также приводят в порядок остановочные пункты и делают тротуары, которых не было раньше.</p>
